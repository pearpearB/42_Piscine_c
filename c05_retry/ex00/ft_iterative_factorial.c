int	ft_iterative_factorial(int nb)
{
	int	i;
	int	num;

	i = 1;
	num = 1;
	if (nb < 0)
		return (0);
	else
	{
		while (i <= nb)
		{
			num = num * i;
			i++;
		}
	}
	return (num);
}
