void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	tmp;

	i = 0;
	while (i < size)
	{
		tmp = tab[size - 1];
		tab[size - 1] = tab[i];
		tab[i] = tmp;
		i++;
		size--;
	}
}
